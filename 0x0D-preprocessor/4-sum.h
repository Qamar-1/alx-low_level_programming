#ifndef SUM_H
#define SUM_H

/**
 *SUM - Macro to compute the sum of two numbers.
 *@x: The first number.
 *@y: The second number.
 *This macro takes two numeric arguments, x and y, and computes their sum.
 */
#define SUM(x, y) ((x) + (y))

#endif /* SUM_H */

